# Ejercicios de Hilos


'''
Ejercicio 1
Crea un programa que imprima los números del 1 al 10 utilizando hilos, de
manera que cada hilo imprima un número. Asegúrate de que los números se impriman
en orden.
'''

'''
Ejercicio 2
Crea un programa que calcule la suma de los números del 1 al 100 utilizando hilos. 
Cada hilo debe calcular la suma de una parte del rango y al final se debe imprimir 
la suma total.
'''

'''
Ejercicio 3
Crea un programa que simule una cuenta regresiva de 10 segundos utilizando hilos. 
Cada hilo debe imprimir el número correspondiente en orden descendente y esperar 
un segundo antes de imprimir el siguiente número.
'''

'''
Ejercicio 4
Crea un programa que simule una carrera entre dos corredores utilizando hilos. 
Cada corredor debe imprimir su nombre y avanzar un paso en cada iteración. 
El primer corredor en llegar a la meta debe ser anunciado como el ganador.
'''

'''
Ejercicio 5
Crea un programa que simule un semáforo utilizando hilos. El semáforo debe tener 
tres estados: rojo, amarillo y verde. Cada estado debe durar 2 segundos y se debe 
imprimir el estado actual del semáforo en cada iteración.
'''

'''
Ejercicio 6
Enunciado: Crea un programa que simule una barrera utilizando hilos. El programa 
debe crear 5 hilos, y cada hilo debe imprimir un mensaje cuando llegue a la barrera. 
Después de que todos los hilos hayan llegado, se debe imprimir un mensaje indicando 
que todos han llegado.
'''

'''
Ejercicio 7
Crea un programa que simule una cola utilizando hilos. El programa debe tener dos 
hilos: uno para encolar elementos y otro para desencolar elementos. Se debe imprimir 
cada acción realizada y asegurarse de que no haya conflicto en el acceso a la cola.
'''

'''
Ejercicio 8
Enunciado: Crea un programa que genere 5 hilos y cada uno imprima los números del 
1 al 5 en orden ascendente, con un retraso de 0.5 segundos entre cada número.
'''

'''
Ejercicio 9
Crea un programa que simule una sala de cine con 3 asientos disponibles. Se deben 
crear 5 hilos, donde cada hilo representa a una persona intentando reservar un 
asiento. El programa debe imprimir si cada persona pudo reservar el asiento o no.
'''

'''
Ejercicio 10
Enunciado: Crea un programa que simule un juego de ping-pong entre dos jugadores 
utilizando hilos. El programa debe imprimir el nombre del jugador actual y alternar 
entre los dos jugadores en cada iteración. El juego debe tener un total de 10 puntos 
y se debe imprimir el nombre del ganador al final.
'''