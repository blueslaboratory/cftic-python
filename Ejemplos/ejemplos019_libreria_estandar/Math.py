# Modulo Math


import math

print('\n*** 1.Funciones matematicas basicas ***')
print(math.sqrt(16))
print(math.pow(2, 3))
print(math.floor(6.9)) # redondea hacia abajo


print('\n*** 2.Funciones trigonometricas ***')
# En radianes
print(math.sin(math.pi/2))
print(math.cos(0))
print(math.radians(90))


print('\n*** 3.Funciones logaritmicas ***')
print(math.log(10))
print(math.log10(100))


print('\n*** 4.Funciones especiales ***')
print(math.factorial(5))
print(math.exp(1)) # e**1
print(math.pi)